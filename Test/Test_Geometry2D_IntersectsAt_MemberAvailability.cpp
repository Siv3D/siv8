namespace
{
	template <class A, class B, class = void>
	struct HasMemberIntersectsAt : std::false_type {};

	template <class A, class B>
	struct HasMemberIntersectsAt<A, B, std::void_t<decltype(
		std::declval<const A&>().intersectsAt(std::declval<const B&>()))>>
		: std::is_same<decltype(std::declval<const A&>().intersectsAt(
			std::declval<const B&>())), Optional<Array<Vec2>>> {};
}

