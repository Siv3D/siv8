namespace
{
	template <class A, class B, class = void>
	struct HasGeometry2DDistance : std::false_type {};

	template <class A, class B>
	struct HasGeometry2DDistance<A, B, std::void_t<decltype(
		Geometry2D::Distance(std::declval<const A&>(), std::declval<const B&>()))>>
		: std::is_same<decltype(Geometry2D::Distance(
			std::declval<const A&>(), std::declval<const B&>())), double> {};

	template <class A, class B, class = void>
	struct HasGeometry2DClosestPoints : std::false_type {};

	template <class A, class B>
	struct HasGeometry2DClosestPoints<A, B, std::void_t<decltype(
		Geometry2D::ClosestPoints(std::declval<const A&>(), std::declval<const B&>()))>>
		: std::is_same<decltype(Geometry2D::ClosestPoints(
			std::declval<const A&>(), std::declval<const B&>())), Optional<ClosestPoints2D>> {};
}

