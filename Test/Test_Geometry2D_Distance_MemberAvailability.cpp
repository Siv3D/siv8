namespace
{
	template <class A, class B, class = void>
	struct HasDistanceToMember : std::false_type {};

	template <class A, class B>
	struct HasDistanceToMember<A, B, std::void_t<decltype(
		std::declval<const A&>().distanceTo(std::declval<const B&>()))>>
		: std::is_same<decltype(std::declval<const A&>().distanceTo(
			std::declval<const B&>())), double> {};

	template <class A, class B, class = void>
	struct HasClosestPointsMember : std::false_type {};

	template <class A, class B>
	struct HasClosestPointsMember<A, B, std::void_t<decltype(
		std::declval<const A&>().closestPoints(std::declval<const B&>()))>>
		: std::is_same<decltype(std::declval<const A&>().closestPoints(
			std::declval<const B&>())), Optional<ClosestPoints2D>> {};
}

