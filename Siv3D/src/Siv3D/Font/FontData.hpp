		[[nodiscard]]
		String getGlyphNameByGlyphIndex(GlyphIndex glyphIndex);

		////////////////////////////////////////////////////////////////
		//
		//	getXAdvanceByGlyphIndex
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		double getXAdvanceByGlyphIndex(GlyphIndex glyphIndex);

		////////////////////////////////////////////////////////////////
		//
		//	getYAdvanceByGlyphIndex
