		////////////////////////////////////////////////////////////////
		//
		//	isInitialized
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		bool isInitialized() const noexcept;

		////////////////////////////////////////////////////////////////
		//
		//	toString
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		String toString() const;

		////////////////////////////////////////////////////////////////
		//
		//	addFallbackFont
		//
		////////////////////////////////////////////////////////////////

		void addFallbackFont(Font::IDType handleID);

		////////////////////////////////////////////////////////////////
		//
		//	getFallbackFontIDs
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		const Array<Font::IDType>& getFallbackFontIDs() const noexcept;

		////////////////////////////////////////////////////////////////
		//
		//	getFallbackFontID
		//
