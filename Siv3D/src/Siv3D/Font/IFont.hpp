# pragma once
# include <Siv3D/Common.hpp>
# include <Siv3D/Array.hpp>
# include <Siv3D/Font.hpp>
# include <Siv3D/FontFaceProperties.hpp>
# include <Siv3D/TextStyle.hpp>

namespace s3d
{
	struct FontFaceInfo;
	struct ITextEffect;
	class PixelShader;

	class SIV3D_NOVTABLE ISiv3DFont
	{
	public:

		////////////////////////////////////////////////////////////////
		//
		//	Create
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		static ISiv3DFont* Create();

		////////////////////////////////////////////////////////////////
		//
		//	(destructor)
		//
		////////////////////////////////////////////////////////////////

		virtual ~ISiv3DFont() = default;

		////////////////////////////////////////////////////////////////
		//
		//	init
		//
		////////////////////////////////////////////////////////////////

		virtual void init() = 0;

		////////////////////////////////////////////////////////////////
		//
		//	getFontFaces
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		virtual Array<FontFaceProperties> getFontFaces(FilePathView path) const = 0;

		////////////////////////////////////////////////////////////////
		//
		//	create
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		virtual Font::IDType create(Typeface typeface, FontMethod fontMethod, int32 baseSize, FontStyle style) = 0;

		[[nodiscard]]
		virtual Font::IDType create(FilePathView path, size_t faceIndex, StringView styleName, FontMethod fontMethod, int32 baseSize, FontStyle style) = 0;

		////////////////////////////////////////////////////////////////
		//
		//	release
		//
		////////////////////////////////////////////////////////////////

		virtual void release(Font::IDType handleID) = 0;

		////////////////////////////////////////////////////////////////
		//
		//	addFallbackFont
		//
		////////////////////////////////////////////////////////////////

		virtual void addFallback(Font::IDType handleID, const Font& font) = 0;

		////////////////////////////////////////////////////////////////
		//
		//	getInfo
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		virtual const FontFaceInfo& getInfo(Font::IDType handleID) = 0;

		////////////////////////////////////////////////////////////////
		//
		//	setTabSize
		//
		////////////////////////////////////////////////////////////////

		virtual void setTabSize(Font::IDType handleID, int32 tabSize) = 0;

		////////////////////////////////////////////////////////////////
		//
		//	setBufferThickness
		//
		////////////////////////////////////////////////////////////////

		virtual void setBufferThickness(Font::IDType handleID, int32 thickness) = 0;

		////////////////////////////////////////////////////////////////
		//
		//	getBufferThickness
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		virtual int32 getBufferThickness(Font::IDType handleID) = 0;

		////////////////////////////////////////////////////////////////
		//
		//	getGlyphIndex
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		virtual GlyphIndex getGlyphIndex(Font::IDType handleID, char32 codePoint, ReadingDirection readingDirection) = 0;

		[[nodiscard]]
		virtual GlyphIndex getGlyphIndex(Font::IDType handleID, StringView ch, ReadingDirection readingDirection) = 0;

		////////////////////////////////////////////////////////////////
		//
		//	getGlyphNameByGlyphIndex
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		virtual String getGlyphNameByGlyphIndex(Font::IDType handleID, GlyphIndex glyphIndex) = 0;

		////////////////////////////////////////////////////////////////
		//
		//	getXAdvanceByGlyphIndex
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		virtual double getXAdvanceByGlyphIndex(Font::IDType handleID, GlyphIndex glyphIndex) = 0;

		////////////////////////////////////////////////////////////////
		//
		//	getYAdvanceByGlyphIndex
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		virtual double getYAdvanceByGlyphIndex(Font::IDType handleID, GlyphIndex glyphIndex) = 0;

		////////////////////////////////////////////////////////////////
		//
		//	getYAdvance
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		virtual double getYAdvance(Font::IDType handleID, StringView ch) = 0;

		////////////////////////////////////////////////////////////////
		//
		//	getResolvedGlyphs
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		virtual Array<ResolvedGlyph> getResolvedGlyphs(Font::IDType handleID, StringView s, ReadingDirection readingDirection, EnableFallback enableFallback, EnableLigatures enableLigatures) = 0;

		////////////////////////////////////////////////////////////////
		//
		//	renderOutlineGlyphByGlyphIndex
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		virtual OutlineGlyph renderOutlineGlyphByGlyphIndex(Font::IDType handleID, GlyphIndex glyphIndex, CloseRing closeRing, ReadingDirection readingDirection) = 0;

		////////////////////////////////////////////////////////////////
		//
		//	renderOutlineGlyphs
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		virtual Array<OutlineGlyph> renderOutlineGlyphs(Font::IDType handleID, StringView s, CloseRing closeRing, EnableLigatures enableLigatures, ReadingDirection readingDirection) = 0;

		////////////////////////////////////////////////////////////////
		//
		//	renderPolygonGlyphByGlyphIndex
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		virtual PolygonGlyph renderPolygonGlyphByGlyphIndex(Font::IDType handleID, GlyphIndex glyphIndex, ReadingDirection readingDirection) = 0;

		////////////////////////////////////////////////////////////////
		//
		//	renderPolygonGlyphs
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		virtual Array<PolygonGlyph> renderPolygonGlyphs(Font::IDType handleID, StringView s, EnableLigatures enableLigatures, ReadingDirection readingDirection) = 0;

		////////////////////////////////////////////////////////////////
		//
		//	renderBitmapGlyphByGlyphIndex
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		virtual BitmapGlyph renderBitmapGlyphByGlyphIndex(Font::IDType handleID, GlyphIndex glyphIndex, ReadingDirection readingDirection) = 0;

		////////////////////////////////////////////////////////////////
		//
		//	renderMSDFGlyphByGlyphIndex
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		virtual MSDFGlyph renderMSDFGlyphByGlyphIndex(Font::IDType handleID, GlyphIndex glyphIndex, ReadingDirection readingDirection) = 0;

		////////////////////////////////////////////////////////////////
		//
		//	preload
		//
		////////////////////////////////////////////////////////////////

		virtual bool preload(Font::IDType handleID, const Array<ResolvedGlyph>& resolvedGlyphs, ReadingDirection readingDirection) = 0;

		////////////////////////////////////////////////////////////////
		//
		//	getTexture
		//
		////////////////////////////////////////////////////////////////

		virtual const Texture& getTexture(Font::IDType handleID) = 0;

		////////////////////////////////////////////////////////////////
		//
		//	getGlyphByGlyphIndex
		//
		////////////////////////////////////////////////////////////////

		virtual Glyph getGlyphByGlyphIndex(Font::IDType handleID, GlyphIndex glyphIndex, ReadingDirection readingDirection) = 0;

		////////////////////////////////////////////////////////////////
		//
		//	getGlyphs
		//
		////////////////////////////////////////////////////////////////

		virtual Array<Glyph> getGlyphs(Font::IDType handleID, StringView s, const Array<ResolvedGlyph>& resolvedGlyphs, ReadingDirection readingDirection) = 0;

		////////////////////////////////////////////////////////////////
		//
		//	getXAdvances
		//
		////////////////////////////////////////////////////////////////

		virtual Array<double> getXAdvances(Font::IDType handleID, StringView s, const Array<ResolvedGlyph>& resolvedGlyphs, double fontSize) = 0;

		////////////////////////////////////////////////////////////////
		//
		//	xAdvanceFallback
		//
		////////////////////////////////////////////////////////////////

		virtual double xAdvanceFallback(Font::IDType handleID, const ResolvedGlyph& resolvedGlyph, double fontSize) = 0;

		////////////////////////////////////////////////////////////////
		//
		//	region
		//
		////////////////////////////////////////////////////////////////

		virtual RectF region(Font::IDType handleID, StringView s, const Array<ResolvedGlyph>& resolvedGlyphs, const Vec2& pos, double fontSize, const TextStyle& textStyle, ReadingDirection readingDirection) = 0;

		////////////////////////////////////////////////////////////////
		//
		//	draw
		//
		////////////////////////////////////////////////////////////////

		virtual RectF draw(Font::IDType handleID, StringView s, const Array<ResolvedGlyph>& resolvedGlyphs, const Vec2& pos, double fontSize, const TextStyle& textStyle, const ITextEffect& textEffect, ReadingDirection readingDirection) = 0;

		////////////////////////////////////////////////////////////////
		//
		//	regionBase
		//
		////////////////////////////////////////////////////////////////

		virtual RectF regionBase(Font::IDType handleID, StringView s, const Array<ResolvedGlyph>& resolvedGlyphs, const Vec2& pos, double fontSize, const TextStyle& textStyle, ReadingDirection readingDirection) = 0;

		////////////////////////////////////////////////////////////////
		//
		//	drawBase
		//
		////////////////////////////////////////////////////////////////

		virtual RectF drawBase(Font::IDType handleID, StringView s, const Array<ResolvedGlyph>& resolvedGlyphs, const Vec2& pos, double fontSize, const TextStyle& textStyle, const ITextEffect& textEffect, ReadingDirection readingDirection) = 0;

		////////////////////////////////////////////////////////////////
		//
		//	regionBaseFallback
		//
		////////////////////////////////////////////////////////////////

		virtual std::pair<double, double> regionBaseFallback(Font::IDType handleID, const ResolvedGlyph& resolvedGlyph, const Vec2& pos, double fontSize, const TextStyle& textStyle, ReadingDirection readingDirection) = 0;

		////////////////////////////////////////////////////////////////
		//
		//	drawBaseFallback
		//
		////////////////////////////////////////////////////////////////

		virtual std::pair<double, double> drawBaseFallback(Font::IDType handleID, const ResolvedGlyph& resolvedGlyph, const Vec2& pos, double fontSize, const TextStyle& textStyle, const ITextEffect& textEffect, int32 index, int32 totalGlyphCount, ReadingDirection readingDirection) = 0;

		////////////////////////////////////////////////////////////////
		//
		//	fitsRect
		//
		////////////////////////////////////////////////////////////////

		virtual bool fitsRect(Font::IDType handleID, StringView s, const Array<ResolvedGlyph>& resolvedGlyphs, const RectF& rect, double fontSize, const TextStyle& textStyle, ReadingDirection readingDirection) = 0;
		
		////////////////////////////////////////////////////////////////
		//
		//	drawRect
		//
		////////////////////////////////////////////////////////////////

		virtual bool drawRect(Font::IDType handleID, StringView s, const Array<ResolvedGlyph>& resolvedGlyphs, const RectF& rect, double fontSize, const TextStyle& textStyle, const ITextEffect& textEffect, ReadingDirection readingDirection) = 0;

		////////////////////////////////////////////////////////////////
		//
		//	getFontShader
		//
		////////////////////////////////////////////////////////////////

		virtual const PixelShader& getFontShader(FontMethod method, TextStyle::Type type) const = 0;
	};
}
