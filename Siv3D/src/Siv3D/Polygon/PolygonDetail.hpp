# pragma once
# include <Siv3D/Polygon.hpp>
# include "GeometryCommon.hpp"

namespace s3d
{
	namespace detail
	{
		Polygon ToPolygon(const CwOpenPolygon& polygon);
	}

	struct PolygonData
	{
		// 外周の頂点配列（時計回り）
		Array<Vec2> outer;

		// 穴を構成する頂点配列（反時計回り）の配列
		Array<Array<Vec2>> inners;
	};

	struct PolygonCentroidResult
	{
		Vec2 centroid;
		
		double area;
	};

	class Polygon::PolygonDetail
	{
	public:

		////////////////////////////////////////////////////////////////
		//
		//	(constructor)
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		PolygonDetail() = default;

		[[nodiscard]]
		PolygonDetail(std::span<const Vec2> outer, Array<Array<Vec2>> holes, SkipValidation skipValidation);

		[[nodiscard]]
		PolygonDetail(std::span<const Vec2> outer, Array<Array<Vec2>> holes, const RectF& boundingRect, SkipValidation skipValidation);

		[[nodiscard]]
		PolygonDetail(std::span<const Vec2> outer, Array<TriangleIndex> indices, const RectF& boundingRect, SkipValidation skipValidation);

		[[nodiscard]]
		PolygonDetail(std::span<const Vec2> outer, Array<Array<Vec2>> holes, Array<Float2> vertices, Array<TriangleIndex> indices, const RectF& boundingRect, SkipValidation skipValidation);

		[[nodiscard]]
		PolygonDetail(std::span<const Float2> outer, Array<TriangleIndex> indices, const RectF& boundingRect);

		////////////////////////////////////////////////////////////////
		//
		//	isEmpty
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		bool isEmpty() const noexcept;

		////////////////////////////////////////////////////////////////
		//
		//	outer
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		const Array<Vec2>& outer() const noexcept;

		////////////////////////////////////////////////////////////////
		//
		//	inners
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		const Array<Array<Vec2>>& inners() const noexcept;

		////////////////////////////////////////////////////////////////
		//
		//	vertices
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		const Array<Float2>& vertices() const noexcept;

		////////////////////////////////////////////////////////////////
		//
		//	indices
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		const Array<TriangleIndex>& indices() const noexcept;

		////////////////////////////////////////////////////////////////
		//
		//	boundingRect
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		const RectF& boundingRect() const noexcept;

		////////////////////////////////////////////////////////////////
		//
		//	moveBy
		//
		////////////////////////////////////////////////////////////////

		void moveBy(Vec2 v) noexcept;

		////////////////////////////////////////////////////////////////
		//
		//	rotateAt
		//
		////////////////////////////////////////////////////////////////

		void rotateAt(Vec2 pos, double angle);

		////////////////////////////////////////////////////////////////
		//
		//	transform
		//
		////////////////////////////////////////////////////////////////

		void transform(double s, double c, const Vec2& pos);

		////////////////////////////////////////////////////////////////
		//
		//	scaleFromOrigin
		//
		////////////////////////////////////////////////////////////////

		void scaleFromOrigin(double s);

		void scaleFromOrigin(Vec2 s);

		////////////////////////////////////////////////////////////////
		//
		//	scaleFrom
		//
		////////////////////////////////////////////////////////////////

		void scaleFrom(Vec2 pos, double s);

		void scaleFrom(Vec2 pos, Vec2 s);

		////////////////////////////////////////////////////////////////
		//
		//	area
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		double area() const noexcept;

		////////////////////////////////////////////////////////////////
		//
		//	perimeter
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		double perimeter() const noexcept;

		////////////////////////////////////////////////////////////////
		//
		//	centroid
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		Optional<PolygonCentroidResult> centroid() const;

		////////////////////////////////////////////////////////////////
		//
		//	computeConvexHull
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		Polygon computeConvexHull() const;

		////////////////////////////////////////////////////////////////
		//
		//	computeMiterBufferPolygon
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		Polygon computeMiterBufferPolygon(double distance) const;

		////////////////////////////////////////////////////////////////
		//
		//	computeRoundBufferPolygon
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		Polygon computeRoundBufferPolygon(double distance, const QualityFactor& qualityFactor) const;

		////////////////////////////////////////////////////////////////
		//
		//	simplified
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		Polygon simplified(double maxDistance) const;

		////////////////////////////////////////////////////////////////
		//
		//	append
		//
		////////////////////////////////////////////////////////////////

		bool append(const RectF& other);

		bool append(const Polygon& other);

		////////////////////////////////////////////////////////////////
		//
		//	draw
		//
		////////////////////////////////////////////////////////////////

		void draw(const Optional<Float2>& offset, const ColorF& color) const;

		void draw(const Optional<Float2>& offset, const PatternParameters& pattern) const;

		////////////////////////////////////////////////////////////////
		//
		//	drawTransformed
		//
		////////////////////////////////////////////////////////////////

		void drawTransformed(double s, double c, const Vec2& pos, const ColorF& color) const;

		void drawTransformed(double s, double c, const Vec2& pos, const PatternParameters& pattern) const;

		////////////////////////////////////////////////////////////////
		//
		//	drawFrame
		//
		////////////////////////////////////////////////////////////////

		void drawFrame(const Optional<Float2>& offset, double thickness, const ColorF& color) const;

		void drawFrame(const Optional<Float2>& offset, double thickness, const PatternParameters& pattern) const;

		////////////////////////////////////////////////////////////////
		//
		//	drawWireframe
		//
		////////////////////////////////////////////////////////////////

		void drawWireframe(const Optional<Float2>& offset, double thickness, const ColorF& color) const;

		void drawWireframe(const Optional<Float2>& offset, double thickness, const PatternParameters& pattern) const;

		////////////////////////////////////////////////////////////////
		//
		//	toCwOpenPolygon
		//
		////////////////////////////////////////////////////////////////

		CwOpenPolygon toCwOpenPolygon() const;

		////////////////////////////////////////////////////////////////
		//
		//	Validate
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		static PolygonFailureType Validate(std::span<const Vec2> outer, const Array<Array<Vec2>>& holes);

		////////////////////////////////////////////////////////////////
		//
		//	Correct
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		static Array<Polygon> Correct(std::span<const Vec2> outer, const Array<Array<Vec2>>& holes);

	private:

		PolygonData m_polygon;

		Array<Float2> m_vertices;

		Array<TriangleIndex> m_indices;

		RectF m_boundingRect = RectF::Empty();
	};
}
