# pragma once
# include <memory>
# include <Siv3D/Array.hpp>
# include <Siv3D/Effect.hpp>

namespace s3d
{
	class EffectData
	{
	public:

		struct Null {};

		////////////////////////////////////////////////////////////////
		//
		//	(constructor)
		//
		////////////////////////////////////////////////////////////////

		EffectData(Null);

		EffectData(double maxLifeTimeSec, bool sortingEnabled);

		////////////////////////////////////////////////////////////////
		//
		//	isInitialized
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		bool isInitialized() const noexcept;

		////////////////////////////////////////////////////////////////
		//
		//	add
		//
		////////////////////////////////////////////////////////////////

		void add(std::unique_ptr<IEffect>&& effect, double initialElapsedTimeSec);

		////////////////////////////////////////////////////////////////
		//
		//	size
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		size_t size() const noexcept;

		////////////////////////////////////////////////////////////////
		//
		//	pause
		//
		////////////////////////////////////////////////////////////////

		void pause() noexcept;

		////////////////////////////////////////////////////////////////
		//
		//	isPaused
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		bool isPaused() const noexcept;

		////////////////////////////////////////////////////////////////
		//
		//	resume
		//
		////////////////////////////////////////////////////////////////

		void resume() noexcept;

		////////////////////////////////////////////////////////////////
		//
		//	setSpeed, getSpeed
		//
		////////////////////////////////////////////////////////////////

		void setSpeed(double speed) noexcept;

		[[nodiscard]]
		double getSpeed() const noexcept;

		////////////////////////////////////////////////////////////////
		//
		//	setMaxLifeTime, getMaxLifeTime
		//
		////////////////////////////////////////////////////////////////

		void setMaxLifeTime(double maxLifeTimeSec) noexcept;

		[[nodiscard]]
		double getMaxLifeTime() const noexcept;

		////////////////////////////////////////////////////////////////
		//
		//	setSortingEnabled, isSortingEnabled
		//
		////////////////////////////////////////////////////////////////

		void setSortingEnabled(bool enabled) noexcept;

		[[nodiscard]]
		bool isSortingEnabled() const noexcept;

		////////////////////////////////////////////////////////////////
		//
		//	advanceTime
		//
		////////////////////////////////////////////////////////////////

		void advanceTime(uint64 deltaUs) noexcept;

		////////////////////////////////////////////////////////////////
		//
		//	update
		//
		////////////////////////////////////////////////////////////////

		void update();

		////////////////////////////////////////////////////////////////
		//
		//	clear
		//
		////////////////////////////////////////////////////////////////

		void clear();

	private:

		struct EffectHolder
		{
			std::unique_ptr<IEffect> effect;
			
			double elapsedTimeSec = 0.0;
		};

		Array<EffectHolder> m_effects;

		Array<EffectHolder> m_pendingEffects;

		double m_maxLifeTimeSec = Effect::DefaultMaxLifeTimeSec;

		double m_timeScale = 1.0;

		double m_lastDeltaSec = 0.0;

		bool m_sortingEnabled = false;

		bool m_sortDirty = false;

		bool m_paused = false;

		bool m_locked = false;

		bool m_clearRequested = false;
	};
}
