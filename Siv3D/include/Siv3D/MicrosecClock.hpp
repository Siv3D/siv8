# pragma once
# include "Common.hpp"
# include "Time.hpp"

namespace s3d
{
	////////////////////////////////////////////////////////////////
	//
	//	MicrosecClock
	//
	////////////////////////////////////////////////////////////////

	/// @brief マイクロ秒カウンター
	/// @remark 経過時間をマイクロ秒で計測します。
	class MicrosecClock
	{
	public:

		////////////////////////////////////////////////////////////////
		//
		//	us
		//
		////////////////////////////////////////////////////////////////

		/// @brief 経過時間（マイクロ秒）を返します。
		/// @return 経過時間（マイクロ秒）
		[[nodiscard]]
		int64 us() const noexcept;

		////////////////////////////////////////////////////////////////
		//
		//	log
		//
		////////////////////////////////////////////////////////////////

		/// @brief ログに経過時間を出力します。
		void log() const;

		////////////////////////////////////////////////////////////////
		//
		//	console
		//
		////////////////////////////////////////////////////////////////

		/// @brief コンソールに経過時間を出力します。
		void console() const;

		////////////////////////////////////////////////////////////////
		//
		//	print
		//
		////////////////////////////////////////////////////////////////

		/// @brief 経過時間をデバッグ表示します。
		void print() const;

	private:

		const int64 m_start = Time::GetMicrosec();
	};
}

