# pragma once
# include <expected>
# include "FormatData.hpp"

namespace s3d
{
	////////////////////////////////////////////////////////////////
	//
	//	Unexpected
	//
	////////////////////////////////////////////////////////////////

	template <class E>
	using Unexpected = std::unexpected<E>;

	////////////////////////////////////////////////////////////////
	//
	//	BadExpectedAccess
	//
	////////////////////////////////////////////////////////////////

	template <class E>
	using BadExpectedAccess = std::bad_expected_access<E>;

	////////////////////////////////////////////////////////////////
	//
	//	Unexpect_t
	//
	////////////////////////////////////////////////////////////////

	using Unexpect_t = std::unexpect_t;

	////////////////////////////////////////////////////////////////
	//
	//	unexpect
	//
	////////////////////////////////////////////////////////////////

	inline constexpr Unexpect_t unexpect{ std::unexpect };

	////////////////////////////////////////////////////////////////
	//
	//	Result
	//
	////////////////////////////////////////////////////////////////

	/// @brief 結果またはエラーを表現する型。
	template <class Type, class E>
	using Result = std::expected<Type, E>;

	////////////////////////////////////////////////////////////////
	//
	//	Err
	//
	////////////////////////////////////////////////////////////////

	template <class E>
	struct Err final : std::unexpected<std::decay_t<E>>
	{
		using std::unexpected<std::decay_t<E>>::unexpected;
	};

	template <class E>
	Err(E&&) -> Err<E>;

	////////////////////////////////////////////////////////////////
	//
	//	Formatter
	//
	////////////////////////////////////////////////////////////////

	template <class Type, class E>
	void Formatter(FormatData& formatData, const Result<Type, E>& value);
}

