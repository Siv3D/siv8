# pragma once
# include "Common.hpp"
# include "Image.hpp"
# include "PDFLinkInfo.hpp"

namespace s3d
{
	////////////////////////////////////////////////////////////////
	//
	//	PDFPage
	//
	////////////////////////////////////////////////////////////////

	/// @brief PDF のページのレンダリング結果
	struct PDFPage
	{
		/// @brief レンダリングされたページの画像データ
		Image image;

		/// @brief ページの拡大率
		double scale = 1.0;

		/// @brief ページの回転
		/// @remark 0: 回転なし、1: 90 度時計回り、2: 180 度、3: 270 度時計回り
		int32 rotation = 0;

		/// @brief ページ内のリンク注釈情報
		Array<PDFLinkInfo> links;

		////////////////////////////////////////////////////////////////
		//
		//	operator bool
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		explicit operator bool() const noexcept;

		////////////////////////////////////////////////////////////////
		//
		//	isEmpty
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		bool isEmpty() const noexcept;

		////////////////////////////////////////////////////////////////
		//
		//	size
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		Size size() const noexcept;

		////////////////////////////////////////////////////////////////
		//
		//	clear
		//
		////////////////////////////////////////////////////////////////

		void clear() noexcept;
	};
}

