# pragma once
# include <memory>
# include "Common.hpp"
# include "OSCReceivedMessage.hpp"

namespace s3d
{
	class IPv4Endpoint;

	////////////////////////////////////////////////////////////////
	//
	//	OSCReceiver
	//
	////////////////////////////////////////////////////////////////

	/// @brief OSC 受信クラス
	class OSCReceiver
	{
	public:

		////////////////////////////////////////////////////////////////
		//
		//	(constructor)
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		OSCReceiver();

		/// @brief OSC メッセージの受信を開始します。
		/// @param endpoint 受信する IPv4 エンドポイント
		[[nodiscard]]
		OSCReceiver(const IPv4Endpoint& endpoint);

		////////////////////////////////////////////////////////////////
		//
		//	(destructor)
		//
		////////////////////////////////////////////////////////////////

		/// @brief OSC メッセージの受信を終了します。
		~OSCReceiver();

		////////////////////////////////////////////////////////////////
		//
		//	hasMessages
		//
		////////////////////////////////////////////////////////////////

		/// @brief これまで受信して未取得のメッセージがあるかを返します。
		/// @return 受信して未取得のメッセージがある場合 true, それ以外の場合は false
		[[nodiscard]]
		bool hasMessages();

		////////////////////////////////////////////////////////////////
		//
		//	num_messages
		//
		////////////////////////////////////////////////////////////////

		/// @brief これまで受信して未取得のメッセージの個数を返します。
		/// @return 受信して未取得のメッセージの個数
		[[nodiscard]]
		size_t num_messages();

		////////////////////////////////////////////////////////////////
		//
		//	pop
		//
		////////////////////////////////////////////////////////////////

		/// @brief 受信して未取得のメッセージのうち最も古いものを取得します。
		/// @throw Error 未取得のメッセージがない場合
		/// @return 未取得のメッセージのうち最も古いもの
		[[nodiscard]]
		OSCReceivedMessage pop();

		////////////////////////////////////////////////////////////////
		//
		//	clear
		//
		////////////////////////////////////////////////////////////////

		/// @brief これまで受信して未取得のメッセージをすべて破棄します。
		void clear();

	private:

		class OSCReceiverDetail;

		std::shared_ptr<OSCReceiverDetail> pImpl;
	};
}
