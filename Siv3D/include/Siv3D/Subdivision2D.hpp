# pragma once
# include "Common.hpp"
# include "Array.hpp"
# include "PointVector.hpp"
# include "2DShapes.hpp"
# include "VoronoiCell.hpp"

namespace s3d
{
	////////////////////////////////////////////////////////////////
	//
	//	Subdivision2D
	//
	////////////////////////////////////////////////////////////////

	class Subdivision2D
	{
	public:
		
		using EdgeID	= int32;

		using VertexID	= int32;

		enum class PointLocation : int32
		{
			/// @brief エラー
			Error = -2,

			/// @brief Subdivision bounding rect の外
			OutsideRect = -1,

			/// @brief Facet 内
			Inside = 0,

			/// @brief 頂点の重複
			OnVertex = 1,

			/// @brief Edge 上
			OnEdge = 2,
		};

		enum class EdgeType : int32
		{
			NextAroundOrigin = 0x00,

			NextAroundDst = 0x22,

			PreviousAroundOrigin = 0x11,

			PreviousAroundDst = 0x33,

			NextAroundLeft = 0x13,

			NextAroundRight = 0x31,

			PreviousAroundLeft = 0x20,

			PreviousAroundRight = 0x02
		};

		struct NearestVertex
		{
			VertexID id;
			
			Vec2 pos;
		};

		////////////////////////////////////////////////////////////////
		//
		//	(constructor)
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		Subdivision2D() = default;

		[[nodiscard]]
		explicit Subdivision2D(const RectF& rect);

		[[nodiscard]]
		explicit Subdivision2D(const RectF& rect, const Array<Vec2>& points);

		////////////////////////////////////////////////////////////////
		//
		//	isEmpty
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		bool isEmpty() const noexcept;

		////////////////////////////////////////////////////////////////
		//
		//	operator bool
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		explicit operator bool() const noexcept;

		////////////////////////////////////////////////////////////////
		//
		//	reset
		//
		////////////////////////////////////////////////////////////////

		void reset(const RectF& rect);

		////////////////////////////////////////////////////////////////
		//
		//	addPoint
		//
		////////////////////////////////////////////////////////////////

		VertexID addPoint(const Vec2& point);

		////////////////////////////////////////////////////////////////
		//
		//	addPoints
		//
		////////////////////////////////////////////////////////////////

		void addPoints(const Array<Vec2>& points);

		////////////////////////////////////////////////////////////////
		//
		//	findNearestVertex
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		Optional<NearestVertex> findNearestVertex(const Vec2& point);

		////////////////////////////////////////////////////////////////
		//
		//	computeDelaunayEdges
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		Array<Line> computeDelaunayEdges() const;

		void computeDelaunayEdges(Array<Line>& edgeList) const;

		////////////////////////////////////////////////////////////////
		//
		//	computeDelaunayTriangles
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		Array<Triangle> computeDelaunayTriangles() const;

		void computeDelaunayTriangles(Array<Triangle>& triangleList) const;

		////////////////////////////////////////////////////////////////
		//
		//	computeVoronoiCells
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		Array<VoronoiCell> computeVoronoiCells();

		void computeVoronoiCells(Array<VoronoiCell>& cellList);

		void computeVoronoiCells(const Array<VertexID>& vertexIDs, Array<VoronoiCell>& cellList);

		////////////////////////////////////////////////////////////////
		//
		//	computeLeadingEdges
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		Array<EdgeID> computeLeadingEdges() const;

		void computeLeadingEdges(Array<EdgeID>& leadingEdgeList) const;

		////////////////////////////////////////////////////////////////
		//
		//	getVertex
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		Vec2 getVertex(VertexID vertex, EdgeID* firstEdge = nullptr) const;

		////////////////////////////////////////////////////////////////
		//
		//	getEdge
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		EdgeID getEdge(EdgeID edge, EdgeType nextEdgeType) const;

		////////////////////////////////////////////////////////////////
		//
		//	nextEdge
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		EdgeID nextEdge(EdgeID edge) const;

		////////////////////////////////////////////////////////////////
		//
		//	rotateEdge
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		EdgeID rotateEdge(EdgeID edge, int32 rotate) const;

		////////////////////////////////////////////////////////////////
		//
		//	symEdge
		//
		////////////////////////////////////////////////////////////////

		[[nodiscard]]
		EdgeID symEdge(EdgeID edge) const;

		////////////////////////////////////////////////////////////////
		//
		//	edgeBegin
		//
		////////////////////////////////////////////////////////////////

		VertexID edgeBegin(EdgeID edge, Vec2* beginPos = nullptr) const;

		////////////////////////////////////////////////////////////////
		//
		//	edgeEnd
		//
		////////////////////////////////////////////////////////////////

		VertexID edgeEnd(EdgeID edge, Vec2* endPos = nullptr) const;

	private:

		struct Vertex
		{
			EdgeID firstEdge = 0;

			int32 type = -1;

			Vec2 pt{ 0, 0 };

			Vertex() = default;

			constexpr Vertex(const Vec2& _pt, bool _isVirtual, EdgeID _firstEdge = 0) noexcept;

			[[nodiscard]]
			constexpr bool isVirtual() const noexcept;

			[[nodiscard]]
			constexpr bool isFree() const noexcept;
		};

		struct QuadEdge
		{
			EdgeID next[4] = {};

			VertexID pt[4] = {};

			QuadEdge() = default;

			explicit constexpr QuadEdge(EdgeID edgeidx) noexcept;

			[[nodiscard]]
			constexpr bool isFree() const noexcept;
		};

		struct Internal
		{
			Array<Vertex> vertices;

			Array<QuadEdge> qEdges;

			size_t addedPoints = 0;

			int32 freeQEdge = 0;

			VertexID freePoint = 0;

			bool validGeometry = false;

			EdgeID recentEdge = 0;

			Vec2 topLeft{ 0, 0 };

			Vec2 bottomRight{ 0, 0 };

			[[nodiscard]]
			constexpr bool isEmpty() const noexcept;

			[[nodiscard]]
			constexpr RectF rect() const noexcept;

			void clear() noexcept;

		} m_internal;

		////////////////////////////////////////////////////////////////
		//
		//	locate
		//
		////////////////////////////////////////////////////////////////

		PointLocation locate(const Vec2& pt, EdgeID& _edge, VertexID& _vertex);

		////////////////////////////////////////////////////////////////
		//
		//	newEdge
		//
		////////////////////////////////////////////////////////////////

		EdgeID newEdge();

		////////////////////////////////////////////////////////////////
		//
		//	deleteEdge
		//
		////////////////////////////////////////////////////////////////

		void deleteEdge(EdgeID edge);

		////////////////////////////////////////////////////////////////
		//
		//	newPoint
		//
		////////////////////////////////////////////////////////////////

		VertexID newPoint(const Vec2& pt, bool isVirtual, EdgeID firstEdge = 0);

		////////////////////////////////////////////////////////////////
		//
		//	deletePoint
		//
		////////////////////////////////////////////////////////////////

		void deletePoint(VertexID vidx);

		////////////////////////////////////////////////////////////////
		//
		//	setEdgePoints
		//
		////////////////////////////////////////////////////////////////

		void setEdgePoints(EdgeID edge, VertexID orgPt, VertexID dstPt);

		////////////////////////////////////////////////////////////////
		//
		//	splice
		//
		////////////////////////////////////////////////////////////////

		void splice(EdgeID edgeA, EdgeID edgeB);

		////////////////////////////////////////////////////////////////
		//
		//	connectEdges
		//
		////////////////////////////////////////////////////////////////

		EdgeID connectEdges(EdgeID edgeA, EdgeID edgeB);

		////////////////////////////////////////////////////////////////
		//
		//	swapEdges
		//
		////////////////////////////////////////////////////////////////

		void swapEdges(EdgeID edge);

		////////////////////////////////////////////////////////////////
		//
		//	isRightOf
		//
		////////////////////////////////////////////////////////////////

		int32 isRightOf(const Vec2& pt, EdgeID edge) const;

		////////////////////////////////////////////////////////////////
		//
		//	clearVoronoi
		//
		////////////////////////////////////////////////////////////////

		void clearVoronoi();

		////////////////////////////////////////////////////////////////
		//
		//	calcVoronoi
		//
		////////////////////////////////////////////////////////////////

		void calcVoronoi();
	};
}

